public struct Turno
{
    public string Dia;
    public string Hora;
    public string Especialidad;
    public string Observacion;

    public Turno(string dia, string hora, string especialidad, string observacion)
    {
        Dia = dia;
        Hora = hora;
        Especialidad = especialidad;
        Observacion = observacion;
    }
}
